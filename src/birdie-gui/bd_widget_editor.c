static void
editor_init(bd_id id, void *state)
{
	struct editor *e = state;
	e->mono = 1;            /* a code/music editor wants fixed-width by default */
	e->sel_anchor = -1;
	e->find_cur = -1;
	e->ac_min = 2;          /* auto-complete after this many word chars */
	bd_set(id, BD_PREF_W_I, 280, BD_PREF_H_I, 140, BD_PAD_I, 4, BD_END);
}

static void
editor_destroy(bd_id id, void *state)
{
	(void)id;
	struct editor *e = state;
	free(e->buf);
	free(e->runs);
	free(e->find_needle);
	free(e->find_starts);
	e->buf = NULL;
	e->runs = NULL;
	e->find_needle = NULL;
	e->find_starts = NULL;
}

/* draw one line [a,b) at (x0,top), segmenting by style run */
static void
editor_draw_line(struct editor *e, int a, int b, float x0, int top,
    const bd_theme *th)
{
	int lh = e_line_h(e);
	float penx = x0;
	int seg = a;
	while (seg < b) {
		bd_rich_style st = e_style_at(e, seg);
		int q = e_next(e->buf, b, seg);
		while (q < b && e_style_eq(e_style_at(e, q), st))
			q = e_next(e->buf, b, q);

		char tmp[1024];
		int n = q - seg;
		if (n >= (int)sizeof tmp) n = (int)sizeof tmp - 1;
		memcpy(tmp, e->buf + seg, (size_t)n);
		tmp[n] = '\0';
		int font = e_font(e, st);          /* mono + true bold/italic face */
		float w = bd_draw_text_width_styled(tmp, font);

		uint32_t fg = st.fg ? st.fg : th->text;
		int y = top;
		if (st.flags & BD_RT_SUPER) y -= lh / 4;
		else if (st.flags & BD_RT_SUB) y += lh / 4;

		if (st.bg)
			bd_draw_rect(penx, (float)top, w, (float)lh, st.bg);
		bd_draw_text_styled(tmp, penx, (float)y, fg, font);
		if (st.flags & BD_RT_UNDERLINE)
			bd_draw_rect(penx, (float)(y + lh - 2), w, 1.0f, fg);
		if (st.flags & BD_RT_STRIKE)
			bd_draw_rect(penx, (float)(y + lh / 2), w, 1.0f, fg);

		penx += w;
		seg = q;
	}
}

static void
editor_render(bd_id id, void *state)
{
	struct editor *e = state;
	const bd_theme *th = bd_gui_theme();
	const bd_backend *be = bd_backend_get();
	int focused = (bd_focused() == id);

	int x, y, w, h;
	bd_widget_rect(id, &x, &y, &w, &h);
	bd_draw_rect(x, y, w, h, th->press);
	bd_draw_rect_lines(x, y, w, h, focused ? th->focus : th->border);

	int pad = bd_get_i(id, BD_PAD_I);
	int ix = x + pad, iy = y + pad;
	int iw = w - 2 * pad, ih = h - 2 * pad;
	int lh = e_line_h(e);

	/* caret line/x, then keep it in view */
	int cls = e_line_start(e, e->cursor);
	int caret_line = e_line_index(e, e->cursor);
	float caret_px = e_span_px(e, cls, e->cursor);
	int caret_top = caret_line * lh;
	if (caret_top - e->scroll_y < 0) e->scroll_y = caret_top;
	if (caret_top + lh - e->scroll_y > ih) e->scroll_y = caret_top + lh - ih;
	if (e->scroll_y < 0) e->scroll_y = 0;
	if (caret_px - e->scroll_x > (float)iw) e->scroll_x = caret_px - (float)iw;
	if (caret_px - e->scroll_x < 0.0f) e->scroll_x = caret_px;
	if (e->scroll_x < 0.0f) e->scroll_x = 0.0f;

	bd_draw_flush();
	be->scissor(ix, iy, iw, ih);

	/* selection span (sorted); a per-line rect is drawn behind the text */
	int has_sel = focused && e->sel_anchor >= 0 && e->sel_anchor != e->cursor;
	int s0 = e->sel_anchor < e->cursor ? e->sel_anchor : e->cursor;
	int s1 = e->sel_anchor < e->cursor ? e->cursor : e->sel_anchor;

	int li = 0, pos = 0;
	for (;;) {
		int le = e_row_end(e, pos);
		int top = iy + li * lh - e->scroll_y;
		if (top + lh >= iy && top <= iy + ih) {
			if (has_sel && s0 <= le && s1 >= pos) {
				int a = s0 > pos ? s0 : pos;
				int b = s1 < le ? s1 : le;
				float xa = e_span_px(e, pos, a);
				float xb = e_span_px(e, pos, b);
				int hx = ix + (int)(xa - e->scroll_x);
				int hw = (int)(xb - xa);
				if (s1 > le) hw += 6;   /* selected newline stub */
				bd_draw_rect((float)hx, (float)top, (float)hw,
				    (float)lh, th->select);
			}
			editor_draw_line(e, pos, le, (float)ix - e->scroll_x, top, th);
		}
		if (le >= e->len) break;
		pos = le + 1;
		li++;
	}

	if (focused && !e->locked) {
		double t = bd_time();
		if (((int)(t * 2.0)) % 2 == 0) {
			int cx = ix + (int)(caret_px - e->scroll_x);
			int cy = iy + caret_top - e->scroll_y;
			bd_draw_rect((float)cx, (float)cy, 2.0f, (float)lh, th->text_hi);
		}
	}

	bd_draw_flush();
	be->scissor_off();

	/* autocomplete popup: a floating list anchored under the word, drawn
	 * after the text scissor so it can overlay the following lines */
	if (focused && e->ac_open && e->ac_n > 0) {
		int prow = (int)bd_draw_line_height() + 4;
		float ws_px = e_span_px(e, cls, e->ac_anchor);   /* word-start x */
		int px = ix + (int)(ws_px - e->scroll_x);
		int caret_y = iy + caret_top - e->scroll_y;

		int pw = 140;
		for (int i = 0; i < e->ac_n; i++) {
			int wt = (int)bd_draw_text_width(e->ac_text[i]);
			int wd = e->ac_detail[i][0]
			    ? (int)bd_draw_text_width(e->ac_detail[i]) + 20 : 0;
			int need = wt + wd + 16;
			if (need > pw) pw = need;
		}
		if (pw > w - 8) pw = w - 8;
		int ph = e->ac_n * prow + 2;
		if (px + pw > x + w - 2) px = x + w - 2 - pw;
		if (px < x + 2) px = x + 2;
		int py = caret_y + lh;                     /* below the caret ... */
		if (py + ph > y + h - 2) py = caret_y - ph; /* ... or flip above */
		if (py < y + 2) py = y + 2;

		bd_draw_flush();
		bd_draw_rect((float)px, (float)py, (float)pw, (float)ph, th->widget);
		bd_draw_rect_lines((float)px, (float)py, (float)pw, (float)ph, th->border);
		int base = (prow - (int)bd_draw_line_height()) / 2;
		for (int i = 0; i < e->ac_n; i++) {
			int ry = py + 1 + i * prow;
			if (i == e->ac_sel)
				bd_draw_rect((float)(px + 1), (float)ry,
				    (float)(pw - 2), (float)prow, th->select);
			bd_draw_text(e->ac_text[i], (float)(px + 6),
			    (float)(ry + base), th->text);
			if (e->ac_detail[i][0]) {
				float dw = bd_draw_text_width(e->ac_detail[i]);
				bd_draw_text(e->ac_detail[i],
				    (float)(px + pw - 6) - dw, (float)(ry + base),
				    th->border);
			}
		}
		bd_draw_flush();
	}
}

