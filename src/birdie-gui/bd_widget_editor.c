static unsigned char
e_lc(unsigned char c)
{
	return (c >= 'A' && c <= 'Z') ? (unsigned char)(c + 32) : c;
}

/* Drop every run tagged `layer`, compacting the array. */
static void
e_clear_layer(struct editor *e, int layer)
{
	int w = 0;
	for (int i = 0; i < e->nrun; i++)
		if (e->runs[i].layer != layer)
			e->runs[w++] = e->runs[i];
	e->nrun = w;
}

/* Does the needle match at byte `pos`? (bounds already checked by caller) */
static int
e_match_at(const struct editor *e, int pos, const char *ndl, int nlen,
    unsigned flags)
{
	for (int i = 0; i < nlen; i++) {
		unsigned char a = (unsigned char)e->buf[pos + i];
		unsigned char b = (unsigned char)ndl[i];
		if (flags & BD_FIND_ICASE) { a = e_lc(a); b = e_lc(b); }
		if (a != b)
			return 0;
	}
	if (flags & BD_FIND_WORD) {
		if (pos > 0 && ac_is_word((unsigned char)e->buf[pos - 1]))
			return 0;
		if (pos + nlen < e->len &&
		    ac_is_word((unsigned char)e->buf[pos + nlen]))
			return 0;
	}
	return 1;
}

/* Remove the current-match mark and, if a match is current, add it back at
 * find_starts[find_cur]. */
static void
e_find_mark(struct editor *e)
{
	e_clear_layer(e, E_LAYER_MARK);
	if (e->find_cur < 0 || e->find_cur >= e->find_n || !e->find_needle)
		return;
	int nlen = (int)strlen(e->find_needle);
	int s = e->find_starts[e->find_cur];
	bd_rich_style st = { 0, 0, FIND_CUR_BG };
	e_add_run(e, s, s + nlen, E_LAYER_MARK, st);
}

/* Rescan the buffer for the stored needle: refill find_starts, re-add the
 * FIND-layer highlight for every match, and clamp find_cur. Does not move the
 * caret or re-mark (callers do). */
static void
e_find_rebuild(struct editor *e)
{
	e_clear_layer(e, E_LAYER_FIND);
	e_clear_layer(e, E_LAYER_MARK);
	e->find_n = 0;
	if (!e->find_needle || !e->find_needle[0]) {
		e->find_cur = -1;
		return;
	}
	int nlen = (int)strlen(e->find_needle);
	bd_rich_style st = { 0, 0, FIND_BG };
	for (int p = 0; p + nlen <= e->len; ) {
		if (!e_match_at(e, p, e->find_needle, nlen, e->find_flags)) {
			p++;
			continue;
		}
		if (e->find_n == e->find_startcap) {
			int c = e->find_startcap ? e->find_startcap * 2 : 16;
			int *ns = realloc(e->find_starts, (size_t)c * sizeof *ns);
			if (!ns)
				break;
			e->find_starts = ns;
			e->find_startcap = c;
		}
		e->find_starts[e->find_n++] = p;
		e_add_run(e, p, p + nlen, E_LAYER_FIND, st);
		p += nlen;                 /* non-overlapping matches */
	}
	if (e->find_n == 0)
		e->find_cur = -1;
	else if (e->find_cur < 0 || e->find_cur >= e->find_n)
		e->find_cur = 0;
}

/* Point the caret at the current match so render scrolls it into view. */
static void
e_find_reveal(struct editor *e)
{
	if (e->find_cur < 0 || e->find_cur >= e->find_n)
		return;
	e->cursor = e->find_starts[e->find_cur];
	e->sel_anchor = -1;
}

