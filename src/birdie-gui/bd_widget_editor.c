static void
editor_insert(struct editor *e, unsigned cp)
{
	if (e->locked) return;
	char enc[4];
	int n = e_encode(cp, enc);
	int off = e->cursor;
	e_splice(e, off, 0, enc, n);
	e->cursor = off + n;
}

/* ------------------------------------------------------------------ */
/* autocomplete                                                       */
/* ------------------------------------------------------------------ */

static int
ac_is_word(unsigned char c)
{
	return (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ||
	    (c >= '0' && c <= '9') || c == '_';
}

/* Byte offset where the identifier ending at the caret starts. */
static int
ac_word_start(const struct editor *e)
{
	int p = e->cursor;
	while (p > 0 && ac_is_word((unsigned char)e->buf[p - 1]))
		p--;
	return p;
}

static void
ac_close(struct editor *e)
{
	e->ac_open = 0;
	e->ac_n = 0;
}

/* Recompute the prefix before the caret and (re-)query the provider. Opens the
 * popup when the prefix is long enough and the provider returns items. */
static void
ac_query(bd_id id, struct editor *e)
{
	if (!e->ac_fn || e->locked) { ac_close(e); return; }
	int start = ac_word_start(e);
	int plen = e->cursor - start;
	if (plen < e->ac_min || plen >= AC_TEXTLEN) {
		ac_close(e);
		return;
	}
	char prefix[AC_TEXTLEN];
	memcpy(prefix, e->buf + start, (size_t)plen);
	prefix[plen] = '\0';

	bd_completion items[AC_MAX];
	memset(items, 0, sizeof items);
	int n = e->ac_fn(id, prefix, items, AC_MAX, e->ac_user);
	if (n < 0) n = 0;
	if (n > AC_MAX) n = AC_MAX;
	for (int i = 0; i < n; i++) {
		e->ac_text[i][0] = '\0';
		e->ac_detail[i][0] = '\0';
		if (items[i].text) {
			strncpy(e->ac_text[i], items[i].text, AC_TEXTLEN - 1);
			e->ac_text[i][AC_TEXTLEN - 1] = '\0';
		}
		if (items[i].detail) {
			strncpy(e->ac_detail[i], items[i].detail, AC_DETLEN - 1);
			e->ac_detail[i][AC_DETLEN - 1] = '\0';
		}
	}
	e->ac_n = n;
	e->ac_anchor = start;
	if (e->ac_sel >= n)
		e->ac_sel = 0;
	e->ac_open = n > 0;
}

/* Replace the current word fragment with the highlighted completion. */
static void
ac_accept(struct editor *e)
{
	if (!e->ac_open || e->ac_sel < 0 || e->ac_sel >= e->ac_n || e->locked) {
		ac_close(e);
		return;
	}
	const char *t = e->ac_text[e->ac_sel];
	int tn = (int)strlen(t);
	int start = e->ac_anchor;
	e_splice(e, start, e->cursor - start, t, tn);
	e->cursor = start + tn;
	ac_close(e);
}

/* Sorted selection range [*a,*b); 0 when there is no (non-empty) selection. */
static int
ed_sel_range(struct editor *e, int *a, int *b)
{
	if (e->sel_anchor < 0 || e->sel_anchor == e->cursor)
		return 0;
	*a = e->sel_anchor < e->cursor ? e->sel_anchor : e->cursor;
	*b = e->sel_anchor < e->cursor ? e->cursor : e->sel_anchor;
	return 1;
}

/* Delete the selection (if any), leaving the caret at its start. */
static void
ed_del_sel(struct editor *e)
{
	int a, b;
	if (e->locked || !ed_sel_range(e, &a, &b))
		return;
	e_splice(e, a, b - a, NULL, 0);
	e->cursor = a;
	e->sel_anchor = -1;
}

/* Copy the selection (if any) to the system clipboard. */
static void
ed_copy_sel(struct editor *e)
{
	int a, b;
	const bd_backend *be = bd_backend_get();
	if (!be || !be->clipboard_set || !ed_sel_range(e, &a, &b))
		return;
	char *tmp = malloc((size_t)(b - a) + 1);
	if (!tmp)
		return;
	memcpy(tmp, e->buf + a, (size_t)(b - a));
	tmp[b - a] = '\0';
	be->clipboard_set(tmp);
	free(tmp);
}

/* Byte offset at the pixel point (px,py), mapping through scroll and line. */
static int
editor_offset_at(struct editor *e, bd_id id, int px, int py)
{
	int x, y, w, h;
	bd_widget_rect(id, &x, &y, &w, &h);
	int pad = bd_get_i(id, BD_PAD_I);
	int ix = x + pad, iy = y + pad;
	int lh = e_line_h(e);
	int line = (py - iy + e->scroll_y) / (lh > 0 ? lh : 1);
	if (line < 0)
		line = 0;
	int pos = e_row_start(e, line);
	int le = e_row_end(e, pos);
	return e_col_at_px(e, pos, le, (float)(px - ix) + e->scroll_x);
}

static int
editor_event(bd_id id, void *state, const bd_event *ev)
{
	struct editor *e = state;
	int lh = e_line_h(e);

	switch (ev->type) {
	case BD_EV_MOUSE_SCROLL:
		e->scroll_y -= (int)(ev->scroll_dy * (float)lh);
		if (e->scroll_y < 0) e->scroll_y = 0;
		return 1;

	case BD_EV_MOUSE_DOWN: {
		if (ev->button != BD_MOUSE_LEFT) return 1;
		int prev = e->sel_anchor;
		e->cursor = editor_offset_at(e, id, ev->x, ev->y);
		/* shift-click extends from the old anchor; a plain press
		 * clears the selection, a drag anchors lazily on the first move */
		if ((ev->mods & BD_MOD_SHIFT) && prev >= 0)
			e->sel_anchor = prev;
		else
			e->sel_anchor = -1;
		return 1;
	}
	case BD_EV_MOUSE_MOVE:
		/* drag-select: anchor at the press caret on the first move */
		if (e->sel_anchor < 0)
			e->sel_anchor = e->cursor;
		e->cursor = editor_offset_at(e, id, ev->x, ev->y);
		return 1;
	case BD_EV_MOUSE_UP:
		return 1;

	case BD_EV_CHAR:
		if (ev->codepoint >= 32) {
			ed_del_sel(e);   /* typing replaces a selection */
			editor_insert(e, ev->codepoint);
			/* a word char (re-)queries; anything else dismisses */
			if (e->ac_fn && ev->codepoint < 128 &&
			    ac_is_word((unsigned char)ev->codepoint))
				ac_query(id, e);
			else
				ac_close(e);
		}
		return 1;

	case BD_EV_TEXT_COMMIT:        /* IME / compose / dead-key commit */
		if (!e->locked && ev->text) {
			int n = (int)strlen(ev->text);
			e_splice(e, e->cursor, 0, ev->text, n);
			e->cursor += n;
			if (e->ac_fn)
				ac_query(id, e);
		}
		return 1;

	case BD_EV_KEY_DOWN:
		break;
	default:
		return 0;
	}

	/* autocomplete popup steals navigation/accept keys while open */
	if (e->ac_open) {
		switch (ev->key) {
		case BD_KEY_DOWN:
			e->ac_sel = (e->ac_sel + 1) % e->ac_n;
			return 1;
		case BD_KEY_UP:
			e->ac_sel = (e->ac_sel - 1 + e->ac_n) % e->ac_n;
			return 1;
		case BD_KEY_ENTER:
			ac_accept(e);
			return 1;
		case BD_KEY_ESCAPE:
			ac_close(e);
			return 1;      /* consume, keep focus */
		case BD_KEY_BACKSPACE:
			break;         /* fall through: delete, then re-query below */
		default:
			ac_close(e);   /* caret moves etc. dismiss, then act normally */
			break;
		}
	}

	/* key handling */
	int ls = e_line_start(e, e->cursor);
	int old_cursor = e->cursor;
	int shift = ev->mods & BD_MOD_SHIFT;
	switch (ev->key) {
	case BD_KEY_LEFT:
		e->cursor = e_prev(e->buf, e->cursor);
		break;
	case BD_KEY_RIGHT:
		e->cursor = e_next(e->buf, e->len, e->cursor);
		break;
	case BD_KEY_HOME:
		e->cursor = ls;
		break;
	case BD_KEY_END:
		e->cursor = e_row_end(e, e->cursor);
		break;
	case BD_KEY_UP: {
		if (ls == 0) { e->cursor = 0; break; }
		float gx = e_span_px(e, ls, e->cursor);
		int ps = e_line_start(e, ls - 1);
		e->cursor = e_col_at_px(e, ps, ls - 1, gx);
		break;
	}
	case BD_KEY_DOWN: {
		int le = e_row_end(e, e->cursor);
		if (le >= e->len) { e->cursor = e->len; break; }
		float gx = e_span_px(e, ls, e->cursor);
		int ns = le + 1, ne = e_row_end(e, ns);
		e->cursor = e_col_at_px(e, ns, ne, gx);
		break;
	}
	case BD_KEY_ENTER: {
		int mod = ev->mods & (BD_MOD_SHIFT | BD_MOD_CTRL);
		/* enter_submits: plain Enter submits, modified inserts a newline.
		 * default: Ctrl+Enter submits, everything else inserts a newline. */
		int submit = e->enter_submits ? (mod == 0) : (ev->mods & BD_MOD_CTRL);
		if (submit && e->on_submit)
			e->on_submit(id, e->on_submit_data);
		else {
			ed_del_sel(e);            /* replace any selection */
			editor_insert(e, '\n'); /* editor_insert honors the lock */
		}
		break;
	}
	case 'C':       /* Ctrl-C: copy selection */
		if (ev->mods & BD_MOD_CTRL) {
			ed_copy_sel(e);
			return 1;
		}
		return 0;
	case 'X':       /* Ctrl-X: cut selection */
		if (ev->mods & BD_MOD_CTRL) {
			ed_copy_sel(e);
			ed_del_sel(e);
			return 1;
		}
		return 0;
	case 'V':       /* Ctrl-V: paste at the cursor */
		if (ev->mods & BD_MOD_CTRL) {
			const bd_backend *be = bd_backend_get();
			const char *t = (!e->locked && be->clipboard_get)
			    ? be->clipboard_get() : NULL;
			if (t) {
				ed_del_sel(e);   /* replace any selection */
				int n = (int)strlen(t);
				e_splice(e, e->cursor, 0, t, n);
				e->cursor += n;
			}
			return 1;
		}
		return 0;
	case BD_KEY_BACKSPACE:
		if (e->sel_anchor >= 0 && e->sel_anchor != e->cursor) {
			ed_del_sel(e);
		} else if (!e->locked && e->cursor > 0) {
			int p = e_prev(e->buf, e->cursor);
			e_splice(e, p, e->cursor - p, NULL, 0);  /* cursor -> p */
		}
		if (e->ac_open)                  /* shrink the prefix, re-filter */
			ac_query(id, e);
		break;
	case BD_KEY_DELETE:
		if (e->sel_anchor >= 0 && e->sel_anchor != e->cursor) {
			ed_del_sel(e);
		} else if (!e->locked && e->cursor < e->len) {
			int nx = e_next(e->buf, e->len, e->cursor);
			e_splice(e, e->cursor, nx - e->cursor, NULL, 0);
		}
		break;
	case BD_KEY_ESCAPE:
		return 0;   /* let the toolkit drop focus */
	default:
		return 0;
	}
	/* selection: Shift extends across a caret move; a plain move clears it.
	 * Editing keys manage their own anchor above. */
	switch (ev->key) {
	case BD_KEY_LEFT: case BD_KEY_RIGHT: case BD_KEY_UP:
	case BD_KEY_DOWN: case BD_KEY_HOME: case BD_KEY_END:
		if (shift) {
			if (e->sel_anchor < 0)
				e->sel_anchor = old_cursor;
		} else {
			e->sel_anchor = -1;
		}
		break;
	default:
		break;
	}
	return 1;
}

