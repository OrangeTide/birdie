/*
 * Rich-text editor widget. Plain UTF-8 text in a growable buffer with the
 * BD_TEXT_AREA editing model (caret nav, newline, backspace/delete, click,
 * scroll), plus a list of style runs over byte ranges that the renderer draws
 * segment by segment (per-run fg/bg, underline, strikeout, faux-bold,
 * super/subscript). A row-oriented API and a lock complete the editor.
 *
 * Made by a machine. PUBLIC DOMAIN (CC0-1.0)
 */

/* Style runs are tagged with a layer; composition (e_style_at) resolves
 * attribute-by-attribute in ascending layer order (higher layer wins), so a
 * find highlight can tint the background over syntax colour without erasing
 * it. Within a layer, a later run wins. */
enum {
	E_LAYER_SYNTAX = 0,  /* regenerated by a syntax pass */
	E_LAYER_USER,        /* app style_span / highlight_* */
	E_LAYER_FIND,        /* every find match */
	E_LAYER_MARK,        /* the current find match / a transient mark */
	E_N_LAYERS,
};

struct erun { int start, end; int layer; bd_rich_style style; };

#define AC_MAX     10   /* max completions shown at once */
#define AC_TEXTLEN 64
#define AC_DETLEN  24

#define FIND_BG      0x5A5322FFu   /* every match: muted amber wash */
#define FIND_CUR_BG  0xB5791EFFu   /* the current match: brighter amber */

struct editor {
	char  *buf;
	int    len, cap;
	int    cursor;
	int    sel_anchor;      /* selection anchor byte offset, -1 = none */
	float  scroll_x;
	int    scroll_y;
	int    locked;
	int    mono;            /* fixed-width face (default on) */
	int    enter_submits;   /* plain Enter submits instead of inserting \n */
	bd_callback_fn on_submit;
	void  *on_submit_data;
	struct erun *runs;
	int    nrun, runcap;

	/* find / replace */
	char  *find_needle;     /* current search (malloc'd), NULL = inactive */
	unsigned find_flags;    /* BD_FIND_* */
	int    find_cur;        /* index of the current match, -1 = none */
	int    find_n;          /* match count at the last find */
	int   *find_starts;     /* byte offset of each match */
	int    find_startcap;

	/* autocomplete */
	bd_completer_fn ac_fn;
	void  *ac_user;
	int    ac_min;          /* auto-trigger prefix length (default 2) */
	int    ac_open;
	int    ac_anchor;       /* byte offset where the current word starts */
	int    ac_sel;          /* highlighted item */
	int    ac_n;            /* item count */
	char   ac_text[AC_MAX][AC_TEXTLEN];
	char   ac_detail[AC_MAX][AC_DETLEN];
};

static int editor_type;

/* ------------------------------------------------------------------ */
/* utf-8                                                              */
/* ------------------------------------------------------------------ */

static int e_cont(unsigned char c) { return (c & 0xC0) == 0x80; }

static int
e_prev(const char *s, int p)
{
	if (p <= 0) return 0;
	p--;
	while (p > 0 && e_cont((unsigned char)s[p])) p--;
	return p;
}

static int
e_next(const char *s, int len, int p)
{
	if (p >= len) return len;
	p++;
	while (p < len && e_cont((unsigned char)s[p])) p++;
	return p;
}

static int
e_encode(unsigned cp, char *o)
{
	if (cp < 0x80)    { o[0] = (char)cp; return 1; }
	if (cp < 0x800)   { o[0] = (char)(0xC0|(cp>>6)); o[1]=(char)(0x80|(cp&0x3F)); return 2; }
	if (cp < 0x10000) { o[0]=(char)(0xE0|(cp>>12)); o[1]=(char)(0x80|((cp>>6)&0x3F)); o[2]=(char)(0x80|(cp&0x3F)); return 3; }
	o[0]=(char)(0xF0|(cp>>18)); o[1]=(char)(0x80|((cp>>12)&0x3F));
	o[2]=(char)(0x80|((cp>>6)&0x3F)); o[3]=(char)(0x80|(cp&0x3F));
	return 4;
}

/* ------------------------------------------------------------------ */
/* buffer + style-run editing                                         */
/* ------------------------------------------------------------------ */

static int
e_ensure(struct editor *e, int need)
{
	if (need <= e->cap) return 1;
	int c = e->cap ? e->cap * 2 : 256;
	while (c < need) c *= 2;
	char *p = realloc(e->buf, (size_t)c);
	if (!p) return 0;
	e->buf = p;
	e->cap = c;
	return 1;
}

/* shift one byte offset across an edit at [off,off+dellen) growing by delta */
static int
e_shift(int x, int off, int dellen, int delta)
{
	if (x <= off) return x;
	if (x >= off + dellen) return x + delta;
	return off;
}

/* replace dellen bytes at off with ins[0..inslen); fixes runs + cursor */
static void
e_splice(struct editor *e, int off, int dellen, const char *ins, int inslen)
{
	if (off < 0) off = 0;
	if (off > e->len) off = e->len;
	if (dellen < 0) dellen = 0;
	if (off + dellen > e->len) dellen = e->len - off;

	int newlen = e->len - dellen + inslen;
	if (!e_ensure(e, newlen + 1)) return;
	memmove(e->buf + off + inslen, e->buf + off + dellen,
	    (size_t)(e->len - off - dellen));
	if (inslen) memcpy(e->buf + off, ins, (size_t)inslen);
	e->len = newlen;
	e->buf[e->len] = '\0';

	int delta = inslen - dellen;
	for (int i = 0; i < e->nrun; i++) {
		e->runs[i].start = e_shift(e->runs[i].start, off, dellen, delta);
		e->runs[i].end   = e_shift(e->runs[i].end, off, dellen, delta);
	}
	/* drop emptied runs */
	int w = 0;
	for (int i = 0; i < e->nrun; i++)
		if (e->runs[i].end > e->runs[i].start)
			e->runs[w++] = e->runs[i];
	e->nrun = w;

	e->cursor = e_shift(e->cursor, off, dellen, delta);
	if (e->cursor > e->len) e->cursor = e->len;
}

/* ------------------------------------------------------------------ */
/* rows                                                               */
/* ------------------------------------------------------------------ */

static int
e_row_count(const struct editor *e)
{
	int n = 1;
	for (int i = 0; i < e->len; i++)
		if (e->buf[i] == '\n') n++;
	return n;
}

/* byte offset of the start of `row` (clamped to len) */
static int
e_row_start(const struct editor *e, int row)
{
	int p = 0;
	for (int r = 0; r < row; r++) {
		while (p < e->len && e->buf[p] != '\n') p++;
		if (p < e->len) p++;
		else break;
	}
	return p;
}

static int
e_row_end(const struct editor *e, int off)
{
	while (off < e->len && e->buf[off] != '\n') off++;
	return off;
}

/* start of the line containing pos (scan back to after the previous \n) */
static int
e_line_start(const struct editor *e, int pos)
{
	while (pos > 0 && e->buf[pos - 1] != '\n') pos--;
	return pos;
}

static int
e_line_index(const struct editor *e, int pos)
{
	int n = 0;
	for (int i = 0; i < pos; i++)
		if (e->buf[i] == '\n') n++;
	return n;
}

/* ------------------------------------------------------------------ */
/* style runs                                                         */
/* ------------------------------------------------------------------ */

static void
e_add_run(struct editor *e, int s, int en, int layer, bd_rich_style st)
{
	if (s < 0) s = 0;
	if (en > e->len) en = e->len;
	if (en <= s) return;
	if (e->nrun == e->runcap) {
		int c = e->runcap ? e->runcap * 2 : 8;
		struct erun *p = realloc(e->runs, (size_t)c * sizeof *p);
		if (!p) return;
		e->runs = p;
		e->runcap = c;
	}
	e->runs[e->nrun++] = (struct erun){ s, en, layer, st };
}

/* Combined style at byte pos: compose overlapping runs attribute by
 * attribute. flags OR together; fg and bg each take the value from the
 * highest-layer run that sets them (non-zero), ties broken by insertion
 * order. So a find highlight (bg only, high layer) tints over a syntax
 * colour without clearing the foreground. */
static bd_rich_style
e_style_at(const struct editor *e, int pos)
{
	bd_rich_style st = { 0, 0, 0 };
	int fg_layer = -1, bg_layer = -1;
	for (int i = 0; i < e->nrun; i++) {
		const struct erun *r = &e->runs[i];
		if (r->start > pos || pos >= r->end)
			continue;
		st.flags |= r->style.flags;
		if (r->style.fg && r->layer >= fg_layer) {
			st.fg = r->style.fg; fg_layer = r->layer;
		}
		if (r->style.bg && r->layer >= bg_layer) {
			st.bg = r->style.bg; bg_layer = r->layer;
		}
	}
	return st;
}

static int
e_style_eq(bd_rich_style a, bd_rich_style b)
{
	return a.flags == b.flags && a.fg == b.fg && a.bg == b.bg;
}

/* ------------------------------------------------------------------ */
/* measuring                                                          */
/* ------------------------------------------------------------------ */

/* bd_draw font flags for a run's style, plus the editor's base (mono) face */
static int
e_font(const struct editor *e, bd_rich_style st)
{
	int f = e->mono ? BD_FONT_MONO : 0;
	if (st.flags & BD_RT_BOLD)   f |= BD_FONT_BOLD;
	if (st.flags & BD_RT_ITALIC) f |= BD_FONT_ITALIC;
	return f;
}

/* width of buf[a,b) drawn in `font` */
static float
e_seg_w(const struct editor *e, int a, int b, int font)
{
	char tmp[1024];
	int n = b - a;
	if (n <= 0) return 0.0f;
	if (n >= (int)sizeof tmp) n = (int)sizeof tmp - 1;
	memcpy(tmp, e->buf + a, (size_t)n);
	tmp[n] = '\0';
	return bd_draw_text_width_styled(tmp, font);
}

/* style-aware width of buf[a,b): segment by run, measure each in its face */
static float
e_span_px(const struct editor *e, int a, int b)
{
	float w = 0.0f;
	int p = a;
	while (p < b) {
		bd_rich_style st = e_style_at(e, p);
		int q = e_next(e->buf, b, p);
		while (q < b && e_style_eq(e_style_at(e, q), st))
			q = e_next(e->buf, b, q);
		w += e_seg_w(e, p, q, e_font(e, st));
		p = q;
	}
	return w;
}

static int
e_col_at_px(const struct editor *e, int start, int end, float target)
{
	int pos = start;
	float w = 0.0f;
	while (pos < end) {
		int nx = e_next(e->buf, end, pos);
		float cw = e_seg_w(e, pos, nx, e_font(e, e_style_at(e, pos)));
		if (w + cw * 0.5f >= target) return pos;
		w += cw;
		pos = nx;
	}
	return end;
}

static int
e_line_h(const struct editor *e)
{
	int h = (int)bd_draw_line_height_styled(e->mono ? BD_FONT_MONO : 0);
	return h > 0 ? h : 14;
}

