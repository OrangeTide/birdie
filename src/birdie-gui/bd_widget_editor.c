/* ---- find / replace ---- */

int
bd_editor_find(bd_id id, const char *needle, unsigned flags)
{
	struct editor *e = editor_of(id);
	if (!e) return 0;
	free(e->find_needle);
	e->find_needle = NULL;
	if (needle && needle[0]) {
		int n = (int)strlen(needle);
		e->find_needle = malloc((size_t)n + 1);
		if (e->find_needle)
			memcpy(e->find_needle, needle, (size_t)n + 1);
	}
	e->find_flags = flags;
	int caret = e->cursor;
	e->find_cur = 0;
	e_find_rebuild(e);
	if (e->find_n > 0) {
		int idx = 0;                       /* first match at/after the caret */
		while (idx < e->find_n && e->find_starts[idx] < caret)
			idx++;
		e->find_cur = (idx < e->find_n) ? idx : 0;
		e_find_mark(e);
		e_find_reveal(e);
	}
	return e->find_n;
}

int
bd_editor_find_next(bd_id id)
{
	struct editor *e = editor_of(id);
	if (!e || e->find_n <= 0) return -1;
	e->find_cur = (e->find_cur + 1) % e->find_n;
	e_find_mark(e);
	e_find_reveal(e);
	return e->find_cur;
}

int
bd_editor_find_prev(bd_id id)
{
	struct editor *e = editor_of(id);
	if (!e || e->find_n <= 0) return -1;
	e->find_cur = (e->find_cur - 1 + e->find_n) % e->find_n;
	e_find_mark(e);
	e_find_reveal(e);
	return e->find_cur;
}

int
bd_editor_find_count(bd_id id)
{
	struct editor *e = editor_of(id);
	return e ? e->find_n : 0;
}

int
bd_editor_find_current(bd_id id)
{
	struct editor *e = editor_of(id);
	return e ? e->find_cur : -1;
}

void
bd_editor_find_clear(bd_id id)
{
	struct editor *e = editor_of(id);
	if (!e) return;
	free(e->find_needle);
	e->find_needle = NULL;
	e_clear_layer(e, E_LAYER_FIND);
	e_clear_layer(e, E_LAYER_MARK);
	e->find_n = 0;
	e->find_cur = -1;
}

int
bd_editor_replace(bd_id id, const char *repl)
{
	struct editor *e = editor_of(id);
	if (!e || e->locked || !e->find_needle || e->find_cur < 0 ||
	    e->find_cur >= e->find_n)
		return e ? e->find_cur : -1;
	int nlen = (int)strlen(e->find_needle);
	int s = e->find_starts[e->find_cur];
	int rlen = repl ? (int)strlen(repl) : 0;
	e_splice(e, s, nlen, repl, rlen);
	int caret = s + rlen;              /* land past the replacement */
	e->cursor = caret;
	e_find_rebuild(e);                 /* offsets moved; rescan */
	if (e->find_n > 0) {
		int idx = 0;
		while (idx < e->find_n && e->find_starts[idx] < caret)
			idx++;
		e->find_cur = (idx < e->find_n) ? idx : 0;
		e_find_mark(e);
		e_find_reveal(e);
	} else
		e->find_cur = -1;
	return e->find_cur;
}

int
bd_editor_replace_all(bd_id id, const char *repl)
{
	struct editor *e = editor_of(id);
	if (!e || e->locked || !e->find_needle)
		return 0;
	e_find_rebuild(e);
	int n = e->find_n;
	if (n <= 0)
		return 0;
	int nlen = (int)strlen(e->find_needle);
	int rlen = repl ? (int)strlen(repl) : 0;
	for (int i = n - 1; i >= 0; i--)   /* last-to-first keeps offsets valid */
		e_splice(e, e->find_starts[i], nlen, repl, rlen);
	bd_editor_find_clear(id);
	return n;
}
